package ss1_Introduction_to_java.Practices;

public class VariableDataType {
    public static void main(String[] args) {
        int i = 10;  // Khai báo kiểu số nguyên int có giá trị là 10
        float f = 20.5f;  // Khai báo kiểu số thực float có giá tri là 20.5
        double d = 20.5; // Khai báo kiểu số thực double có giá trị là 20.5
        boolean b = true; // Khai báo kiểu boolean có giá trị là true
        char c = 'a'; // Khai báo kiểu kí tự char có giá trị là kí tự a
        String s ="Ha Noi"; // Khai báo kiểu chuỗi có giá trị là Ha Noi
        System.out.println("i = " + i);
        System.out.println("f = " + f);
        System.out.println("d = " + d);
        System.out.println("b = " + b);
        System.out.println("c = " + c);
        System.out.println("s = " + s);
    }
}
